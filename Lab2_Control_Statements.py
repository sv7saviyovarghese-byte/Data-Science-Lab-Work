# Read TXT file
df_txt = pd.read_csv("/content/Facebook Friends.txt", sep="\t", encoding='utf-16')

# Read CSV file
df_csv = pd.read_csv("/content/Facebook Friends.csv")

# Read Excel file
df_excel = pd.read_excel("/content/Facebook Friends.xlsx")

# Display shape comparison
print("TXT Shape:", df_txt.shape)
print("CSV Shape:", df_csv.shape)
print("Excel Shape:", df_excel.shape)

# Compare first few rows
print("\nTXT Head:")
