# ============================================================
# 2. DATA ACCESS
# Load the Facebook datasets (.txt, .csv, .xlsx)
# ============================================================

# File paths (adjust if running locally)
txt_file = "Facebook Friends.txt"
csv_file = "Facebook Friends.csv"
