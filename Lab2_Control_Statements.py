# ============================================================
# 3. DATA EXAMINATION (Using Excel DataFrame Only)
# ============================================================

df = df_excel.copy()

# Check column names
print("\nColumn Names:")
print(df.columns)

# View top rows
print("\nTop 5 Rows:")
print(df.head())

# View bottom rows
print("\nBottom 5 Rows:")
print(df.tail())

# Data types
print("\nData Types:")
print(df.dtypes)

# Check for missing values
print("\nMissing Values Per Column:")
print(df.isnull().sum())

# Check total missing values
print("\nTotal Missing Values:", df.isnull().sum().sum())



# ============================================================
# 4. DATA CLEANING
# Remove commas in numeric columns (e.g., Friends column)
# ============================================================

# Convert object columns that contain numbers with commas
for col in df.columns:
    if df[col].dtype == 'object':
        df[col] = df[col].str.replace(',', '')
        df[col] = pd.to_numeric(df[col], errors='ignore')

print("\nData Types After Cleaning:")
print(df.dtypes)



# ============================================================
# 5. DESCRIPTIVE STATISTICS
# ============================================================

print("\nDescriptive Statistics:")
print(df.describe())

"""
describe() provides:
- count: non-missing observations
- mean: average value
- std: standard deviation
- min: minimum value
- 25%: first quartile
- 50%: median
- 75%: third quartile
- max: maximum value

These describe central tendency and spread of the data.
"""



# ============================================================
# 6. DATA VISUALIZATION
# Histogram and Frequency Plots
# ============================================================

# Select numeric columns only
numeric_cols = df.select_dtypes(include=[np.number]).columns

# ------------------------
# HISTOGRAMS
# ------------------------
for col in numeric_cols:
    plt.figure()
    df[col].hist(bins=20)
    plt.title(f"Histogram of {col}")
    plt.xlabel(col)
    plt.ylabel("Frequency")
    plt.show()

# ------------------------
# FREQUENCY PLOT (BAR PLOT)
# Example for Gender column if binary
# ------------------------

if "Gender" in df.columns:
    plt.figure()
    df["Gender"].value_counts().plot(kind="bar")
    plt.title("Frequency Plot of Gender")
    plt.xlabel("Gender")
    plt.ylabel("Count")
    plt.show()


"""
How visualization helps:
- Identifies skewness
- Detects outliers
- Shows distribution shape
- Highlights dominant categories
- Helps understand spread and variability
"""



# ============================================================
# 7. SAVE OUTPUT AS HTML
